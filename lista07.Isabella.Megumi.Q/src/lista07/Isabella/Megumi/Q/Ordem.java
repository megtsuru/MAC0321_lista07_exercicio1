package lista07.Isabella.Megumi.Q;

public class Ordem extends Ser {
	private String posicao;
	private AcademiaTreinamento academia;
	private boolean passouTesteCavaleiro;
	private boolean telepatia;
	private boolean telecinese;
	private boolean persuasão;
	
	public String getPosicao() {
		return posicao;
	}
	public void setPosicao(String posicao) {
		this.posicao = posicao;
	}
	public AcademiaTreinamento getAcademia() {
		return academia;
	}
	public void setAcademia(AcademiaTreinamento academia) {
		this.academia = academia;
	}
	public boolean isPassouTesteCavaleiro() {
		return passouTesteCavaleiro;
	}
	public void setPassouTesteCavaleiro(boolean passouTesteCavaleiro) {
		this.passouTesteCavaleiro = passouTesteCavaleiro;
	}
	public boolean isTelepatia() {
		return telepatia;
	}
	public void setTelepatia(boolean telepatia) {
		this.telepatia = telepatia;
	}
	public boolean isTelecinese() {
		return telecinese;
	}
	public void setTelecinese(boolean telecinese) {
		this.telecinese = telecinese;
	}
	public boolean isPersuasão() {
		return persuasão;
	}
	public void setPersuasão(boolean persuasão) {
		this.persuasão = persuasão;
	}
}
